#Título
print("***CALCULADORA***")

# Coleta de números
nun1 = int(input("Qual o primeiro número?"))
nun2 = int(input("Qual o segundo número?"))

# Soma dos números
soma = nun1 + nun2

# Subtração dos números
subtracao = nun1 - nun2

# Multiplicação dos números
multiplicacao = nun1 * nun2

# Divisão dos números
divisao = nun1 / nun2

#Exibe todos os resultados
print(f"A soma dos numros é: {soma}.")
print(f"A subtração dos números é: {subtracao}")
print(f"A multiplicação dos números é: {multiplicacao}")
print(f"A divisão dclearos números é: {divisao}")
